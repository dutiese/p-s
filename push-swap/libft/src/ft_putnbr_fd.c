#include "../inc/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	a;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n > 9)
	{
		a = n % 10 + 48;
		n = n / 10;
		ft_putnbr_fd(n, fd);
		write(fd, &a, 1);
	}
	else
	{
		a = 48 + n;
		write(fd, &a, 1);
	}
}
