#include "../inc/libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while ((char)s[i] != (char)c)
	{
		if (!s[i])
			return (0);
		i++;
	}
	return ((char *)&s[i]);
}
